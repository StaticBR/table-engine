/**
 * Mask used to set a border.
 */
export interface IBorderMask {

	/**
	 * Whether to set the border on top.
	 */
	top?: boolean;

	/**
	 * Whether to set the border on bottom.
	 */
	bottom?: boolean;

	/**
	 * Whether to set the border to the left.
	 */
	left?: boolean;

	/**
	 * Whether to set the border to the right.
	 */
	right?: boolean;

}
