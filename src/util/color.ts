/**
 * Representation of a color.
 */
export interface IColor {

	/**
	 * Red value in range [0.0; 1.0].
	 */
	red: number;

	/**
	 * Green value in range [0.0; 1.0].
	 */
	green: number;

	/**
	 * Blue value in range [0.0; 1.0].
	 */
	blue: number;

	/**
	 * Alpha value in range [0.0; 1.0].
	 */
	alpha: number;

}
