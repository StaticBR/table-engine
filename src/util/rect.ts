/**
 * Representation of a rectangle.
 */
export interface IRectangle {
	/**
	 * Left offset of the rectangle.
	 */
	left: number;

	/**
	 * Top offset of the rectangle.
	 */
	top: number;

	/**
	 * Width of the rectangle.
	 */
	width: number;

	/**
	 * Height of the rectangle.
	 */
	height: number;
}
